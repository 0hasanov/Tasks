package warmups;

public class switchExample {

    public static void main(String args[]) {
        int score = 4;
        switch (score) {
            case 1:
                System.out.println("Score is 1");
                break;
            case 2:
                System.out.println("Score is 2");
                break;

            case 4:
                System.out.println("sad");
            default:
                System.out.println("Default Case");
        }
    }
}
